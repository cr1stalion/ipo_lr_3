# Ввод координат точки и радиуса
x = float(input("Введите координату x: "))
y = float(input("Введите координату y: "))
R = float(input("Введите радиус R: "))

# Вычисление расстояния от точки до начала координат
distance = (x**2 + y**2)**0.5

# Проверка попадания точки в круг
if distance <= R:
    print(f"Точка ({x}, {y}) попадает в круг с радиусом {R}")
else:
    print(f"Точка ({x}, {y}) не попадает в круг с радиусом {R}")